package model.DTO;

public class FellowPassengerDTO {
	private String fpPassportNum;
	private String fpKorName;
	private String fpEngName;
	private String fpPh1;
	private int fpRvNum;
	
	public String getFpPassportNum() {
		return fpPassportNum;
	}
	public void setFpPassportNum(String fpPassportNum) {
		this.fpPassportNum = fpPassportNum;
	}
	public String getFpKorName() {
		return fpKorName;
	}
	public void setFpKorName(String fpKorName) {
		this.fpKorName = fpKorName;
	}
	public String getFpEngName() {
		return fpEngName;
	}
	public void setFpEngName(String fpEngName) {
		this.fpEngName = fpEngName;
	}
	public String getFpPh1() {
		return fpPh1;
	}
	public void setFpPh1(String fpPh1) {
		this.fpPh1 = fpPh1;
	}
	public int getFpRvNum() {
		return fpRvNum;
	}
	public void setFpRvNum(int fpRvNum) {
		this.fpRvNum = fpRvNum;
	}
}
